Article 6 : Modalités de réservation d'un espace de travail<br><br>

Bird Office agit en qualité d’intermédiaire entre un Membre Utilisateur et un Membre Partenaire.
Bird Office est mandataire du Membre Partenaire pour l’édition des factures. En ce sens, Bird Office sera compétent pour éditer et délivrer les factures aux Membres Utilisateurs et aux 
Membres Partenaires.
Toutes les factures seront accessibles dans l’espace client des Membres.<br><br>

La réservation se réalise par l'acceptation par un Membre Utilisateur de l'offre de mise à disposition d'un espace de travail.
L'acception de l'offre entraîne la conclusion d'un contrat de mise à disposition temporaire. Toutefois le Membre Partenaire peut choisir de conserver la faculté de confirmer les 
réservations qui lui sont faites, ce qui retardera la conclusion du contrat à la date de confirmation de réservation.<br><br>

En cas de non assujettissement à la TVA, veuillez nous contacter par email à contact@bird-office.com.<br><br>
Pour nous permettre d’en tenir compte, la fourniture d’un certificat ou d’une attestation de non assujettissement est cependant nécessaire. 

Article 7 : Le prix de la mise à disposition<br><br>

Le prix est fixé librement par le Membre Partenaire. Toutefois Bird Office propose un prix indicatif conseillé au Membre Partenaire.
Le prix est exprimé forfaitairement à l'heure, demi-journée ou journée ; le partenaire peut décider de mettre un nombre d'heure minimum pour permettre une réservation.
Le prix de la location d'un espace de travail sur le site de Bird Office ne doit cependant jamais excéder le prix public affiché par le Membre Partenaire.<br><br>

Article 7.1 : Modalités de paiement<br><br>
Le paiement s'effectuera par carte bancaire, prélèvement bancaire ou virement bancaire.

Article 7.2 : Délais de paiement<br><br>
Article 7.2.1 : Délais de paiement dans le cas de réservation d'espaces de travail<br><br>
Le paiement est effectué lors de la réservation, le prélèvement sera différé à 216 heures (9 jours inclus) avant la réalisation de la prestation de service.

 Pour les paiements à moins de 10 jours inclus avant la date de la réalisation de la prestation de service, la totalité du montant sera prélevé quel que soit le mode de paiement.

Pour les paiements à moins de 14 jours inclus avant la date de la réalisation de la prestation de service, ne seront acceptés que les paiements par carte bleue.

Pour les paiements à plus de 10 jours exclus avant la date de la réalisation de la prestation de service :

En cas de paiement par carte bancaire ou prélèvement bancaire, l'intégralité de la somme sera perçue pour les montants inférieurs à 300 EUR hors taxes. Pour les montants supérieurs 
à 300 EUR hors taxes un acompte de 20% est prélevé immédiatement lors de la réservation, le solde sera perçu dans un délai maximum de 10 jours avant le premier jour de la réservation. 
En outre, les prélèvements ne sont possibles que pour des montants d'au moins 300 EUR hors taxes.

En cas de paiement par virement bancaire, l'intégralité de la somme sera perçue pour les montants inférieurs à 1000 EUR hors taxes. Pour les montants supérieurs à 1000 EUR hors taxes 
un acompte de 20% est prélevé immédiatement lors de la réservation, le solde sera à verser dans un délai maximum de 10 jours avant le premier jour de la réservation.

 Une commission de 30% sur le prix total hors taxes sera prélevée par Bird Office. Le montant restant dû est versé en totalité au Membre Partenaire au début du mois suivant celui de 
 la réservation.

 Il est interdit aux Membres de frauder la commission prélevée par Bird Office.<br><br>

Article 7.2.2 : Délais de paiement dans le cas de réservation de prestations de restauration fournies par le Membre Partenaire<br><br>
Les paiements devront obligatoirement avoir été effectués avant les 96 heures (4 jours inclus) de la réalisation de la prestation de service.<br><br>

Article 7.2.3 : Délais de paiement dans le cas de réservation de prestations de restauration fournies par un prestataire externe<br><br>
Les délais de paiement variant selon les prestataires externes, ces derniers seront spécifiés lors de la réservation. <br><br>

Article 8 : Retard de paiement<br><br>

En cas de retard de paiement total ou partiel imputable au Membre Utilisateur, celui-ci sera débiteur, envers la société, d'une pénalité de retard égale à trois fois le taux de l’intérêt 
légal déterminé par la Banque Centrale Européenne (Art. L441-6 Code de Commerce français).

Le taux d’intérêt légal retenu est celui en vigueur au jour de l’exécution de la prestation de service. Cette pénalité est calculée sur le montant toutes taxes comprises de la somme 
restante due, et court à compter de la date d’échéance de la facture sans qu’aucune mise en demeure préalable ne soit nécessaire.

Une indemnité forfaitaire de quarante (40) euros pour frais de recouvrement sera également due en cas de retard.

Le non respect dudit délai de paiement fera encourir à l’égard du Membre Utilisateur une amende administrative pouvant aller jusqu’à deux millions d’euros. 
