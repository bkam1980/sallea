Article 3 : Description du service Bird Office<br><br>

Bird Office agit en tant qu’un intermédiaire entre des Membres Partenaires mettant à disposition des espaces de travail (salle de formation, de conférence et/ou de réunion) et des
 Membres Utilisateurs recherchant des espaces de travail disponibles dans le cadre de leurs activités professionnelles.
