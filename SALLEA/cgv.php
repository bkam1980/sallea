Article 4.1 : Procédure générale pour devenir Membre de Bird Office<br><br>
Toute personne physique ou morale, agissant dans le cadre de son activité professionnelle (société ou entreprise individuelle) et toute association agissant dans le cadre de son activité 
associative, peut devenir Membre du service Bird Office. L'inscription se fait sur le site internet de Bird Office à l'adresse suivante : https://www.bird-office.com/<br><br>

Article 4.2 : Les différentes catégories de Membres<br><br>
Les Membres peuvent être des Membres Utilisateurs ou des Membres Partenaires<br><br>

Article 4.2.1 : Membre Utilisateur <br><br>
Sont dénommés « Membres Utilisateurs » les Membres utilisant le service Bird Office à l'effet de réserver des espaces de travail appartenant à un Membre Partenaire.<br><br>

Article 4.2.2 : Membre Partenaire<br><br>
Sont dénommés « Membres Partenaires » les Membres mettant à disposition des Membres Utilisateurs un ou plusieurs espaces de travail, quelle que soit leur activité : Hôtel, centre d'affaire 
etc.
Les éléments suivants sont par ailleurs obligatoires pour devenir Membre Partenaire :
