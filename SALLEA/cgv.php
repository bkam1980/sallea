Bird Office fait appel à un prestataire externe pour la réalisation desdits services.<br><br>

Article 4 : Devenir Membre du service Bird Office<br><br>

La création d’un compte personnel n’est pas obligatoire pour pouvoir naviguer sur le site de Bird Office.
